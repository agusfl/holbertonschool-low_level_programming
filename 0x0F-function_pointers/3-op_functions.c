/**
 * op_add - add
 * @a: num 1
 * @b: num 2
 * Return: sum
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subtract
 * @a: num 1
 * @b: num 2
 * Return: substraction
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiply
 * @a: num 1
 * @b: num 2
 * Return: multiplication
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divide
 * @a: num 1
 * @b: num 2
 * Return: division
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - modulus for remainder
 * @a: num 1
 * @b: num 2
 * Return: remainder
 */
int op_mod(int a, int b)
{
	return (a % b);
}
